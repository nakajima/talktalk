      if (('A' <= lookahead && lookahead <= 'Z') ||
          lookahead == '_' ||
          ('a' <= lookahead && lookahead <= 'z')) ADVANCE(105);
      END_STATE();
    case 2:
      if (lookahead == '"') ADVANCE(137);
      if (lookahead != 0) ADVANCE(2);
      END_STATE();
    case 3:
